"""
函数的定义:
    def 函数名([参数1,参数2,...,参数n]):
        函数体
        [return 值]
"""

def fn(x,y):
    # x[0] = 1
    x =100
    y[0] = 5 
    print("x : {}, y : {}".format(x,y))
    
a = [100]
b = [200]
# 传递的是不可变类型的数据
# 如果传递的是可变类型的额数据,可能会对外面的变量的值发生改变
fn(a,b) # x = a ; y = b
print("a : {}, b : {}".format(a,b))
    

