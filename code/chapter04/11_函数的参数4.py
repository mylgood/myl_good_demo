# 参数的解包
def fun_a(*,x,y,z):
    print("x:{},y:{},z:{}".format(x,y,z))

# fun_a(1,2,3)
# a = (8,9,10)
# 在函数调用的时候需要解包操作,把元组解包为对应的参数
# fun_a(*a)
fun_a(x=10,y=20,z=30)
d ={"x":40,"y":50,"z":60}
# 对字典d进行拆包为关键字参数
fun_a(**d)
# *args 在函数定义的时候, 不定长的非关键字参数
# **keywords 在函数定义的时候,不定长的关键字参数
# *a  把元组解包为对应的参数
# **d 把字典d拆包为对应的关键字参数
